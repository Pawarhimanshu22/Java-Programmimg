class Arithmatic_00 {
    public static void main(String[] args) {
        int num1 = 70;
        int num2 = 20;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 / num2);

        System.out.println(num1 % num2);
        System.out.println(10 % 2);
        System.out.println(100 % 2); //0
        System.out.println(5 % 2); //1
        System.out.println(57 % 20);
        System.out.println(19 % 20); //19
    }
}